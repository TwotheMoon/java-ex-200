package 변수;

public class 변수8 {

	public static void main(String[] args) {
		// 2개의 실숫값을 입력받아 그 합과 평균을 구하는 프로그램
		// 실행결과 : x값 : 9.75
		//		    y값 : 2.5
		//			합계는 12.25입니다.
		//			평균은 6.125입니다.
		
		double x = 9.75;
		double y = 2.5;
		System.out.println("x값 :" + x );
		System.out.println("y값 : " + y);
		System.out.println("합계는 : " + (x + y) + "입니다.");
		System.out.println("평균은 : " + (x + y)/2 + "입니다.");

	}

}
