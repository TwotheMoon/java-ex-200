package 변수;

public class 변수1 {

	public static void main(String[] args) {
		// 정수82와 17의 합과 차를 구하는 프로그램 실행결과 : 99
		//										  65
		System.out.println(82 + 17);
		System.out.println(82 - 17);
		
		// 정수82와 17의 합과 차를 구하는 프로그램 실행결과 : 82 + 17 = 99
		//										  82 - 17 = 65
		System.out.println("82 + 17 = " + (82 + 17));
		System.out.println("82 - 17 = " + (82 - 17));
	}

}
