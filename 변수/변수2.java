package 변수;

public class 변수2 {

	public static void main(String[] args) {
		// 2개의 변수 x와 y의 합과 평균을 구하자		실행결과 : x값은 63입니다.
		//											 y값은 18입니다.
		//											 합계는 81입니다.
		//											 평균은 40입니다.
		int x = 63;
		int y = 18;
		System.out.println("x값은 " + x + "입니다.");
		System.out.println("y값은 " + y + "입니다.");
		System.out.println("합계는 " + (x + y) + "입니다.");
		System.out.println("평균은 " + (x + y)/2 + "입니다.");
	}

}
