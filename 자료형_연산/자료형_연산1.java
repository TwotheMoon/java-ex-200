package 자료형_연산;

public class 자료형_연산1 {

	public static void main(String[] args) {
		// 8진수 12, 10진수 12,16 진수 12를 각각 10진수로 표시하는 프로그램
		// 실행 결과 : 8진수 12는 10진수로 10입니다
		//		   : 10진수 12는 10진수로 12입니다.
		//		   : 16진수 12는 10진수로 18 입니다.
		
		System.out.println("8진수 12는 10진수로" + 012 + "입니다.");
		System.out.println("10진수 12는 10진수로" + 10 + "입니다");
		System.out.println("16진수 12는 10진수로" + 0x12 + "입니다.");
	}

}
